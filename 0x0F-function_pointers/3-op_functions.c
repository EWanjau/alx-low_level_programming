/**
* op_add - returns sum
 * @a: input
 * @b:input
 * Return: sum
*/
int op_add(int a, int b)
{
    return (a + b);
}

/**
*op_sub - difference of two numbers
 * @a: input
 * @b: input
 * Return: difference
*/
int op_sub(int a, int b)
{
    return (a - b);
}

/**
* op_mul - multiplacation of two numbers
 * @a: input
 * @b: input
 * Return: multiplacation
*/
int op_mul(int a, int b)
{
    return (a * b);
}

/**
* op_div - division of two numbers
 * @a: input
 * @b: input
 * Return: division
*/
